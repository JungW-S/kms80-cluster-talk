let lastWheelStepAt = 0;
let wheelAccumulator = 0;
let wheelResetTimer = null;

window.addEventListener("keydown", (event) => {
  if (activeConstructionStep()) {
    if (event.key === "ArrowRight" || event.key === " " || event.key === "PageDown") {
      if (navigateConstructionIframe(1)) {
        event.preventDefault();
        return;
      }
      event.preventDefault();
      renderStep(currentStep + 1);
      return;
    }
    if (event.key === "ArrowLeft" || event.key === "PageUp") {
      if (navigateConstructionIframe(-1)) {
        event.preventDefault();
        return;
      }
      event.preventDefault();
      renderStep(currentStep - 1);
      return;
    }
  }
  if (event.key === "ArrowRight" || event.key === " " || event.key === "PageDown") {
    event.preventDefault();
    renderStep(currentStep + 1);
  }
  if (event.key === "ArrowLeft" || event.key === "PageUp") {
    event.preventDefault();
    renderStep(currentStep - 1);
  }
});

window.addEventListener("wheel", (event) => {
  const rawDelta = Math.abs(event.deltaY) >= Math.abs(event.deltaX) ? event.deltaY : event.deltaX;
  if (Math.abs(rawDelta) < 2) return;

  const now = performance.now();
  const modeScale = event.deltaMode === 1 ? 18 : event.deltaMode === 2 ? window.innerHeight : 1;
  const primaryDelta = rawDelta * modeScale;
  const direction = primaryDelta > 0 ? 1 : -1;
  const wheelThreshold = 70;
  const wheelCooldown = 120;

  event.preventDefault();

  if (wheelAccumulator && Math.sign(wheelAccumulator) !== direction) {
    wheelAccumulator = 0;
  }
  wheelAccumulator += primaryDelta;

  window.clearTimeout(wheelResetTimer);
  wheelResetTimer = window.setTimeout(() => {
    wheelAccumulator = 0;
  }, 360);

  if (Math.abs(wheelAccumulator) < wheelThreshold || now - lastWheelStepAt < wheelCooldown) {
    return;
  }

  lastWheelStepAt = now;
  const stepCount = Math.min(3, Math.floor(Math.abs(wheelAccumulator) / wheelThreshold));
  if (activeConstructionStep()) {
    wheelAccumulator -= direction * stepCount * wheelThreshold;
    if (navigateConstructionIframe(direction, {
      stepCount,
      speedMultiplier: 0.58,
      force: true
    })) {
      return;
    }
    renderStep(currentStep + direction);
    return;
  }

  wheelAccumulator -= direction * stepCount * wheelThreshold;
  renderStep(currentStep + direction * stepCount);
}, { passive: false });

window.addEventListener("message", (event) => {
  if (event.data?.type !== "wdelta-boundary") return;
  if (!activeConstructionStep()) return;
  const direction = event.data.direction > 0 ? 1 : -1;
  renderStep(currentStep + direction);
});

function initialStepFromUrl() {
  const params = new URLSearchParams(window.location.search);
  const value = Number.parseInt(params.get("step") ?? "1", 10);
  if (Number.isNaN(value)) return 0;
  return value - 1;
}

