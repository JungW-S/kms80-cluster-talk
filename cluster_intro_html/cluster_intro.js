function applyVisual(mode) {
  resetVisual();

  if (mode === "focus") {
    els.node2.classList.add("focus-ring");
    els.focusMutationLabel.classList.add("visible");
  }

  if (mode === "paths") {
    els.seedView.classList.add("copy-layout");
  }

  if (mode === "quiver-mutated") {
    compactOriginalSeed();
    els.seedView.classList.add("mutation-layout");
  }

  if (mode === "involutive") {
    compactOriginalSeed();
    els.seedView.classList.add("variable-layout", "involutive-layout");
  }

  if (mode === "variable-mutated") {
    compactOriginalSeed();
    els.seedView.classList.add("variable-layout");
  }

  if (mode === "one-step-sequence") {
    compactOriginalSeed();
    els.seedView.classList.add("variable-layout", "involutive-layout", "seed-graph-layout");
    els.oneStepView.classList.add("visible", "stage-sequence");
  }

  if (mode === "one-step-expanded") {
    els.seedView.classList.add("pattern-stage");
    els.oneStepView.classList.add("visible", "stage-full", "stage-expanded");
  }

  if (mode === "pattern-variables") {
    els.clusterSlide.classList.add("show-cluster-vars");
    els.seedView.classList.add("pattern-stage");
    els.oneStepView.classList.add("visible", "stage-full", "stage-expanded", "stage-variables");
  }

  if (mode === "cluster-algebra") {
    els.clusterSlide.classList.add("show-cluster-vars", "show-cluster-algebra");
    els.seedView.classList.add("pattern-stage");
    els.oneStepView.classList.add("visible", "stage-full", "stage-expanded", "stage-variables", "stage-algebra");
  }
}

function renderStep(index) {
  currentStep = Math.max(0, Math.min(frames.length - 1, index));
  const step = frames[currentStep];

  els.deck.classList.remove("mode-title", "mode-section", "mode-cluster", "mode-comparison", "mode-static");
  els.sectionSlide.classList.remove("show-motivation");
  els.staticSlides.forEach((slide) => {
    slide.classList.remove("active");
    slide.removeAttribute("data-state");
  });

  if (step.type === "title") {
    els.deck.classList.add("mode-title");
    resetVisual();
  }

  if (step.type === "section") {
    els.deck.classList.add("mode-section");
    els.sectionHeading.textContent = step.title;
    resetVisual();
  }

  if (step.type === "motivation") {
    els.deck.classList.add("mode-section");
    els.sectionHeading.textContent = step.title;
    els.sectionSlide.classList.add("show-motivation");
    resetVisual();
  }

  if (step.type === "cluster") {
    els.deck.classList.add("mode-cluster");
    els.frameTitle.textContent = step.frameTitle ?? step.title;
    els.kicker.textContent = step.kicker;
    els.title.textContent = step.title;
    els.body.innerHTML = step.body;
    applyVisual(step.mode);
  }

  if (step.type === "comparison") {
    els.deck.classList.add("mode-comparison");
    els.frameTitle.textContent = step.title;
    resetVisual();
  }

  if (step.type === "static") {
    els.deck.classList.add("mode-static");
    els.frameTitle.innerHTML = step.title;
    resetVisual();
    const target = document.getElementById(step.slide);
    target?.classList.add("active");
    if (target && step.state) {
      target.dataset.state = step.state;
    }
    if (step.slide === "braid-cluster-slide") {
      syncBraidClusterExample();
    }
    if (step.slide === "pre-comparison-summary-slide") {
      syncPreComparisonSummary();
    }
    if (step.slide === "weave-construction-slide") {
      window.setTimeout(focusConstructionIframe, 80);
    }
  }

  els.counter.textContent = `${currentStep + 1}/${frames.length}`;
  els.prev.disabled = currentStep === 0;
  els.next.disabled = currentStep === frames.length - 1;

  if (window.MathJax?.typesetPromise && step.type === "cluster") {
    const mathTargets = [els.body, els.clusterVariableDisplay, els.clusterAlgebraDisplay];
    window.MathJax.typesetClear?.(mathTargets);
    window.MathJax.typesetPromise(mathTargets).catch(() => {});
  }

  if (window.MathJax?.typesetPromise && step.type === "comparison") {
    window.MathJax.typesetClear?.([els.comparisonSlide]);
    window.MathJax.typesetPromise([els.comparisonSlide]).catch(() => {});
  }

  if (window.MathJax?.typesetPromise && step.type === "static") {
    const target = document.getElementById(step.slide);
    window.MathJax.typesetClear?.([target, els.frameTitle]);
    window.MathJax.typesetPromise([target, els.frameTitle]).catch(() => {});
  }

  if (window.MathJax?.typesetPromise && step.type === "title") {
    window.MathJax.typesetClear?.([els.titleSlide]);
    window.MathJax.typesetPromise([els.titleSlide]).catch(() => {});
  }
}

