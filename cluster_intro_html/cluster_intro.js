function syncBraidClusterExample() {
  const target = document.getElementById("braid-cluster-weave-copy");
  if (!target || target.childElementCount > 0) return;
  const source = document.querySelector("#weave-example-slide .default-example-figure");
  if (!source) return;
  const clone = source.cloneNode(true);
  clone.classList.add("braid-cluster-weave-figure");
  const svg = clone.querySelector(".default-weave-svg");
  if (svg) {
    svg.setAttribute("viewBox", "230 30 330 470");
  }
  target.replaceChildren(clone);
}

function syncPreComparisonSummary() {
  const chainTarget = document.getElementById("summary-chain-copy");
  if (chainTarget && chainTarget.childElementCount === 0) {
    const chainSource = document.querySelector("#seed-chain-slide .seed-source-chain");
    if (chainSource) {
      chainTarget.replaceChildren(chainSource.cloneNode(true));
    }
  }

  const weaveTarget = document.getElementById("summary-weave-copy");
  if (weaveTarget && weaveTarget.childElementCount === 0) {
    const weaveSource = document.querySelector("#weave-example-slide .default-example-figure");
    if (weaveSource) {
      const clone = weaveSource.cloneNode(true);
      const svg = clone.querySelector(".default-weave-svg");
      if (svg) {
        svg.setAttribute("viewBox", "230 30 330 470");
      }
      weaveTarget.replaceChildren(clone);
    }
  }
}

function constructionIframe() {
  return document.querySelector("#weave-construction-slide .wdelta-construction-embed");
}

function activeConstructionStep() {
  return frames[currentStep]?.slide === "weave-construction-slide";
}

function focusConstructionIframe() {
  const iframe = constructionIframe();
  if (!iframe) return;
  iframe.focus({ preventScroll: true });
  try {
    iframe.contentWindow?.focus();
  } catch (_) {
  }
}

function navigateConstructionIframe(direction, options = {}) {
  const iframe = constructionIframe();
  if (!iframe) return false;
  try {
    const api = iframe.contentWindow?.WDELTA_CONSTRUCTION;
    if (!api?.navigateScene) return false;
    const moved = api.navigateScene(direction, options);
    focusConstructionIframe();
    return moved !== false;
  } catch (_) {
    return false;
  }
}

